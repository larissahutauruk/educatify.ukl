  <h2 class="budaya">Budaya Surabaya</h2>
  <div class="bagian">
    <h3>Surabaya Pusat</h3>
    <h4>Kampung Arab</h4>
    <p>Pria biasanya memakai jubah panjang (gamis) dan surban, sedangkan wanita mengenakan jilbab atau hijab yang
      longgar. <br>
      Perayaan: Maulid Nabi Muhammad SAW & kegiatan ziarah.</p>
    <br>
    <h4>Chinatown (Kya-kya Kembang Jepun)</h4>
    <p>Cheongsam atau qipao untuk wanita dan changshan untuk pria.<br>
      Perayaan: Imlek dan Barongsai.</p>
    <br>
    <h3>Surabaya Timur</h3>
    <h4>Kenjeran</h4>
    <p>Pakaian nelayan tradisional yang sederhana, biasanya berbahan katun dan berwarna cerah.</p>
    <br>
    <h3>Surabaya Selatan</h3>
    <h4>Darmo</h4>
    <p>Kebaya untuk wanita dan batik atau beskap untuk pria</p>
    <br>
    <h3>Surabaya Barat</h3>
    <h4>Dukuh Pakis</h4>
    <p>Baju pesaan untuk pria dan kebaya untuk wanita, sering berwarna merah dan hitam.</p>
    <br>
    <h3>Surabaya Utara</h3>
    <h4>Ampel</h4>
    <p>Baju koko dan sarung untuk pria, serta gamis atau jilbab panjang untuk wanita.</p>
    <br>
    <h3><b>Bahasa Asli dan Rumah Adat</b></h3>
    <h4>Bahasa Asli.</h4>
    <p>Bahasa Jawa dan Madura adalah bahasa yang sering digunakan di Surabaya, tergantung pada komunitasnya. <br>
      Bahasa Jawa digunakan dengan berbagai dialek lokal, sementara bahasa Madura digunakan oleh komunitas Madura di
      Surabaya Barat.</p>
    <br>
    <h4>Rumah Adat:</h4>
    <p>Surabaya tidak memiliki rumah adat yang spesifik seperti beberapa daerah lain di Indonesia. <br>
      Namun, rumah tradisional Jawa dengan pendopo sering ditemukan di wilayah yang lebih tua dan memiliki nilai
      sejarah, seperti di Darmo.</p>
    <br>
  </div>

