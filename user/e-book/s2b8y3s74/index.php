  <h3 id="news" class="introduction">Introduction To Surabaya</h3>

  <p class="intro-to">
    Surabaya merupakan kota modern kedua setelah Jakarta dengan tetap memegang budaya dan
    kearifan lokalnya. Kota ini lebih di dominasi oleh suku Jawa yang dikenal ramah dan baik
    hatinya. Tapi jangan mengira pendatang di Surabaya tidak begitu ya.. pendatang di Surabaya
    juga tetap menjaga sopan santunnya sebab itulah Surabaya tetap terbuka dari pendatang luar
    baik dalam negeri dan luar negeri.
    Banyak pendatang luar yang menetap di Surabaya dengan alasan utama mulai dari menempuh
    pendidikan dan bekerja.
    <br>
    Nah, sejak dulu Surabaya terkenal sebagai kota pelabuhan lho. Pelabuhan ini sering dijadikan
    sebagai tempat kegiatan perdagangan dengan negeri Cina Darat, India, dan Timur Tengah
    sehingga membuat Surabaya menjadi tempat berkumpulnya berbagai kebudayaan. Mau tau apa
    saja sosial budaya di Surabaya?
  </p>
